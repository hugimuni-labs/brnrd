"""Render gitignored per-task context files for daemon runners."""

from __future__ import annotations

from pathlib import Path
from typing import Any

from .envs import RunContext
from .task import Task


def write_context_file(
    brr_dir: Path,
    task: Task,
    event: dict[str, Any],
    ctx: RunContext,
    *,
    stream: Any = None,
    event_body: str | None = None,
) -> Path:
    """Write `.brr/runs/<task-id>/context.md` and return its path."""
    context_dir = brr_dir / "runs" / task.id
    context_dir.mkdir(parents=True, exist_ok=True)
    path = context_dir / "context.md"
    path.write_text(
        render_context(task, event, ctx, stream=stream, event_body=event_body),
        encoding="utf-8",
    )
    return path


def render_context(
    task: Task,
    event: dict[str, Any],
    ctx: RunContext,
    *,
    stream: Any = None,
    event_body: str | None = None,
) -> str:
    lines: list[str] = [
        "# brr Run Context",
        "",
        "This file is generated by brr for the active daemon task. It lives in",
        "the gitignored `.brr/` runtime directory and is safe to read, but agents",
        "should not edit it.",
        "",
        "## Task",
        "",
        f"- Task ID: {task.id}",
        f"- Event ID: {task.event_id}",
        f"- Source: {task.source or event.get('source', '')}",
        f"- Status: {task.status}",
        f"- Branch strategy: {task.branch}",
        f"- Environment: {ctx.name}",
        f"- Execution root: {ctx.cwd}",
        f"- Repository root: {ctx.repo_root}",
        f"- Shared runtime dir: {ctx.runtime_dir}",
    ]
    if ctx.base_branch:
        lines.append(f"- Base branch: {ctx.base_branch}")
    if ctx.branch_name:
        lines.append(f"- Current branch: {ctx.branch_name}")
    if ctx.log_file:
        lines.append(f"- Log file: {ctx.log_file}")

    lines.extend([
        "",
        "## Delivery",
        "",
        f"- Response path for the runner: {ctx.response_path_env}",
        f"- Response path on the host: {ctx.response_path_host}",
    ])

    if stream is not None:
        lines.extend(["", "## Workstream", ""])
        lines.append(f"- Stream ID: {getattr(stream, 'id', '') or task.stream_id}")
        for label, attr in (
            ("Title", "title"),
            ("Status", "status"),
            ("Intent", "intent"),
        ):
            value = getattr(stream, attr, "") or ""
            if value:
                lines.append(f"- {label}: {value}")
        summary = (getattr(stream, "summary", "") or "").strip()
        if summary:
            lines.extend(["", "### Current summary", "", summary])
        open_questions = (getattr(stream, "open_questions", "") or "").strip()
        if open_questions:
            lines.extend(["", "### Open questions", "", open_questions])
        reply_route = getattr(stream, "reply_route", None) or {}
        if isinstance(reply_route, dict) and reply_route:
            preferred = reply_route.get("preferred", "")
            selected = reply_route.get("selected", preferred)
            allowed = reply_route.get("allowed", [])
            allowed_str = (
                ", ".join(allowed) if isinstance(allowed, list) else str(allowed)
            )
            lines.append(
                f"- Reply route: preferred={preferred} selected={selected} "
                f"allowed=[{allowed_str}]"
            )

    body = (
        event_body if event_body is not None else event.get("body", "") or ""
    ).strip()
    if body:
        lines.extend(["", "## Original Event Body", "", body])

    task_file = ctx.runtime_dir / "tasks" / f"{task.id}.md"
    lines.extend([
        "",
        "## Runtime Files",
        "",
        f"- Event file: {event.get('_path', '')}",
        f"- Task file: {task_file}",
        f"- Response file: {ctx.response_path_host}",
    ])
    if task.stream_id:
        stream_dir = ctx.runtime_dir / "streams" / task.stream_id
        lines.append(f"- Stream directory: {stream_dir}")
    if ctx.env_state:
        lines.extend(["", "## Environment State", ""])
        for key, value in sorted(ctx.env_state.items()):
            lines.append(f"- {key}: {value}")

    return "\n".join(lines).rstrip() + "\n"
